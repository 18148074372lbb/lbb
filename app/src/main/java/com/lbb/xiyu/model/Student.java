package com.lbb.xiyu.model;

public class Student {
    private String s_id;
    private String s_name;
    private String s_grade;
    private String s_class;
    private String s_spec;
    private String password;
    private String sex;
    private String s_picture;

    public String getS_id() {
        return s_id;
    }

    public void setS_id(String s_id) {
        this.s_id = s_id;
    }

    public String getS_name() {
        return s_name;
    }

    public void setS_name(String s_name) {
        this.s_name = s_name;
    }

    public String getS_grade() {
        return s_grade;
    }

    public void setS_grade(String s_grade) {
        this.s_grade = s_grade;
    }

    public String getS_class() {
        return s_class;
    }

    public void setS_class(String s_class) {
        this.s_class = s_class;
    }

    public String getS_spec() {
        return s_spec;
    }

    public void setS_spec(String s_spec) {
        this.s_spec = s_spec;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getS_picture() {
        return s_picture;
    }

    public void setS_picture(String s_picture) {
        this.s_picture = s_picture;
    }
}
